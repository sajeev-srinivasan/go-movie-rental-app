create table movies(
    id varchar(50) primary key ,
    title varchar(100) not null ,
    year integer not null ,
    rated varchar(10),
    release date,
    runtime int,
    genre varchar(50),
    director varchar(50),
    writer varchar(100),
    actors varchar(150),
    plot varchar(255),
    language varchar(150),
    country varchar(100),
    rating numeric(2,1)
)

